k = imread('man.png');
k = rgb2gray(k);
imtool(k, []);
ft = fftshift(fft2(k));
imtool(log10(abs(ft)), []);
msk = zeros(367, 280);
msk(1 : 131, 141-8 : 141+8) = 1;
msk(235 : 367, 141-8 : 141+8) = 1;
imtool(msk, []);
msk1 = 1 - msk;
imtool(msk1, []);
ft1 = ft.*msk1;
imtool(log10(abs(ft1)), []);
ft2 = ifft2(ifftshift(ft1));
imtool(ft2, []);

k = imread('cameraman.tif');
k = double(k);
k1 = k + 20 * randn(size(k));
imtool(k1, []);
fk = fftshift(fft2(k1));
imtool(log10(abs(fk)), []);
f = butterworth(256, 256, 40);
imtool(f, []);
fk1 = fk.*f;
imtool(log10(abs(fk1)), []);
fk2 = ifft2(ifftshift(fk1));
imtool(fk2, []);
h = fspecial('gaussian',[256 256], 20);
fk1 = fk.*h;
fk2 = ifft2(ifftshift(fk1));
imtool(fk2, []);